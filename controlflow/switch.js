const month = "mar"

switch (month) {
    case "jan":
        console.log("January")
        break;
    case "feb":
        console.log("february")
        break;
    case "mar":
        console.log("March")
        break;
    case "apr":
        console.log("April")
        break;

    default:
        console.log("default value is matched")
        break;
}